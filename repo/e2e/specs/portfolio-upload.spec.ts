test.describe('Portfolio Upload E2E', () => {
  test('merchant uploads JPEG via multipart and gets 202 with pending status', async ({ request }) => {
    const { token } = await login(request, ACCOUNTS.merchant.username, ACCOUNTS.merchant.password);

    // Create multipart form using Playwright's native multipart support
    const res = await request.post(`${BASE}/api/v1/portfolio/upload`, {
      headers: { Authorization: `Bearer ${token}` },
      multipart: {
        file: {
          name: 'e2e-test-photo.jpg',
          mimeType: 'image/jpeg',
          buffer: JPEG_BYTES,
        },
        title: 'E2E Upload Test',
      },
    });

    expect(res.status()).toBe(202);
    const body = await res.json();
    expect(body.id).toBeTruthy();
    expect(body.status).toBe('pending');
    expect(body.title).toBe('E2E Upload Test');
  });

  test('uploaded item appears in portfolio list', async ({ request }) => {
    const { token } = await login(request, ACCOUNTS.merchant.username, ACCOUNTS.merchant.password);

    const listRes = await request.get(`${BASE}/api/v1/portfolio`, {
      headers: { Authorization: `Bearer ${token}` },
    });
    expect(listRes.status()).toBe(200);
    const body = await listRes.json();
    expect(body.data.length).toBeGreaterThan(0);

    // Find the E2E upload
    const uploaded = body.data.find((item: any) => item.title === 'E2E Upload Test');
    expect(uploaded).toBeDefined();
    expect(uploaded.mimeType).toBe('image/jpeg');
  });

  test('upload without auth returns 401', async ({ request }) => {
    const res = await request.post(`${BASE}/api/v1/portfolio/upload`, {
      multipart: {
        file: {
          name: 'test.jpg',
          mimeType: 'image/jpeg',
          buffer: JPEG_BYTES,
        },
      },
    });
    expect(res.status()).toBe(401);
  });

  test('client cannot upload (403)', async ({ request }) => {
    const { token } = await login(request, ACCOUNTS.client.username, ACCOUNTS.client.password);

    const res = await request.post(`${BASE}/api/v1/portfolio/upload`, {
      headers: { Authorization: `Bearer ${token}` },
      multipart: {
        file: {
          name: 'test.jpg',
          mimeType: 'image/jpeg',
          buffer: JPEG_BYTES,
        },
      },
    });
    expect(res.status()).toBe(403);
  });
});
